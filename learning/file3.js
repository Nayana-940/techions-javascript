//JavaScript Variables can be declared in 4 ways:

//Automatically
//Using var
//Using let
//Using const



// Automatically declared variable
x = 10; // No keyword
console.log("Value of x (automatically declared):", x);

// Variable declared with var
var y = 20;
console.log("Value of y (declared with var):", y);

// Variable declared with let
let z = 30;
console.log("Value of z (declared with let):", z);

// Variable declared with const
const a = 40;
console.log("Value of a (declared with const):", a);

